function allStockShow() {
   
    document.getElementById("soldStock").style.animationName = "opacityDown" ;
    document.getElementById("soldStock").style.display = "none" ;
    document.getElementById("toSellStock").style.animationName = "opacityDown" ;
    document.getElementById("toSellStock").style.display = "none" ;
    document.getElementById("allStock").style.animationName = "opacityUp" ;
    document.getElementById("allStock").style.display = "block" ;


    document.getElementById("soldStockBtn").style.backgroundColor = "#20537547" ;
    document.getElementById("toSellStockBtn").style.backgroundColor = "#20537547" ;
    document.getElementById("allStockBtn").style.backgroundColor = "#205375" ;
 
    document.getElementById("soldStockBtn").style.color = "#efefef" ;
    document.getElementById("toSellStockBtn").style.color = "#efefef" ;
    document.getElementById("allStockBtn").style.color = "#F66B0E" ;
 
    
}
